package com.oracle.myacademy.jFoS07.exercicio.fliperma;

public class CategoriaDePremios {
    private String name;
    private int ticketNecessarios;
    private int quantidadeDeItens;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTicketNecessarios() {
        return ticketNecessarios;
    }

    public void setTicketNecessarios(int ticketNecessarios) {
        this.ticketNecessarios = ticketNecessarios;
    }

    public int getQuantidadeDeItens() {
        return quantidadeDeItens;
    }

    public void setQuantidadeDeItens(int quantidadeDeItens) {
        this.quantidadeDeItens = quantidadeDeItens;
    }
}
